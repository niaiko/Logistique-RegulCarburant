package mg.cnaps.models.article;

public class ArticleMod {
	 private int idArticle;
	 private int idTypeArt;
	 private int idClasseArt;
	 private int idDomaineArt;
	 private int idNatureArt;
	 private int idSpeArt;
	 private String libelle;
	 private int stock;
	 
	public int getIdArticle() {
		return idArticle;
	}
	public void setIdArticle(int idArticle) {
		this.idArticle = idArticle;
	}
	public int getIdTypeArt() {
		return idTypeArt;
	}
	public void setIdTypeArt(int idTypeArt) {
		this.idTypeArt = idTypeArt;
	}
	public int getIdClasseArt() {
		return idClasseArt;
	}
	public void setIdClasseArt(int idClasseArt) {
		this.idClasseArt = idClasseArt;
	}
	public int getIdDomaineArt() {
		return idDomaineArt;
	}
	public void setIdDomaineArt(int idDomaineArt) {
		this.idDomaineArt = idDomaineArt;
	}
	public int getIdNatureArt() {
		return idNatureArt;
	}
	public void setIdNatureArt(int idNatureArt) {
		this.idNatureArt = idNatureArt;
	}
	public int getIdSpeArt() {
		return idSpeArt;
	}
	public void setIdSpeArt(int idSpeArt) {
		this.idSpeArt = idSpeArt;
	}
	public String getLibelle() {
		return libelle;
	}
	public void setLibelle(String libelle) {
		this.libelle = libelle;
	}
	public int getStock() {
		return stock;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
}
